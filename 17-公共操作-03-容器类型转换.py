# 容器类型转换

"""tuple()
作⽤：将某个序列转换成元组"""
list1 = [10, 20, 30, 40, 50, 20]
s1 = {100, 200, 300, 400, 500}
print(tuple(list1))
print(tuple(s1))

"""list()
作⽤：将某个序列转换成列表"""
t1 = ('a', 'b', 'c', 'd', 'e')
s1 = {100, 200, 300, 400, 500}
print(list(t1))
print(list(s1))

""" set()
作⽤：将某个序列转换成集合"""
list1 = [10, 20, 30, 40, 50, 20]
t1 = ('a', 'b', 'c', 'd', 'e')
print(set(list1))
print(set(t1))
# 注意：
# 1. 集合可以快速完成列表去重
# 2. 集合不⽀持下标
